--REM Create table PGE_PSPSMAPROWS to include all map rows for PSPS Circuit overview
--REM and segment maps.

CREATE TABLE PGE_PSPSMAPROWS 
(
  CIRCUITID NVARCHAR2(30) 
, CIRCUITNAME NVARCHAR2(50) 
, PSPSNAME NVARCHAR2(15) 
, XMIN NUMBER(15, 2) 
, YMIN NUMBER(15, 2) 
, XMAX NUMBER(15, 2) 
, YMAX NUMBER(15, 2) 
, TOTALSEGMENTLEN NUMBER(15, 2) 
, EXPORTSTATE NUMBER(2, 0) 
, LAYOUTTYPE NUMBER(2, 0) 
, MAPSCALE NUMBER(4, 0) 
, SERVICETOPROCESS NUMBER(2, 0) 
, LASTMODIFIEDDATE DATE 
, MAPID NUMBER(38, 0) 
);

CREATE SEQUENCE PGE_PSPSMAPROWS_MAPID_SEQ
 START WITH     1
 INCREMENT BY   1
 NOCACHE
 NOCYCLE;

--REM Grant Permission to MM_ADMIN users on table PGE_PSPSMAPROWS
GRANT ALL ON PGE_PSPSMAPROWS TO SDE;
GRANT SELECT ON PGE_PSPSMAPROWS_MAPID_SEQ TO SDE;

