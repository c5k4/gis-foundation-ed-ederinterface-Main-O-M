select 'updating related annotation feature classes' from dual;
DECLARE
   cursor push_feeder_type_list is select SOURCEFC.PHYSICALNAME SOURCE_FC_NAME,REL_CLASS.REL_ID, DESTFC.PHYSICALNAME DEST_FC_NAME
from (
  SELECT gi.UUID,gi.PHYSICALNAME 
  from sde.gdb_items gi 
  where gi.physicalname in
      (select 
	   cr1.owner||'.'||cr1.table_name 
	   from sde.column_registry cr1 
	   where cr1.column_name='FEEDERTYPE' 
	   and cr1.table_name<>'CIRCUITSOURCE' 
	   and cr1.table_name in (
	         select cr2.table_name 
			 from sde.column_registry cr2 
			 where cr2.column_name='CIRCUITID'
			 ) 
	  )
) SOURCEFC 
join
( 
select SOURCE_ID,DEST_ID,REL_ID from 
(
  select a.ORIGINID SOURCE_ID, b.ORIGINID DEST_ID, a.UUID REL_ID
  from 
  (
  select UUID,ORIGINID,DESTID
    from sde.gdb_itemrelationships
	where ORIGINID in
	(
	 SELECT gi.UUID 
     from sde.gdb_items gi 
     where gi.physicalname in
      (select 
	   cr1.owner||'.'||cr1.table_name 
	   from sde.column_registry cr1 
	   where cr1.column_name='FEEDERTYPE' 
	   and cr1.table_name<>'CIRCUITSOURCE' 
	   and cr1.table_name in (
	         select cr2.table_name 
			 from sde.column_registry cr2 
			 where cr2.column_name='CIRCUITID'
			 ) 
	  )
	)
	and DESTID in 
   (	select typechecka.UUID from SDE.GDB_ITEMS typechecka where typechecka.TYPE in (select UUID from SDE.GDB_ITEMTYPES where NAME='Relationship Class')
     ) 
  ) a
  left outer join 
  (
  select UUID,ORIGINID,DESTID
    from sde.gdb_itemrelationships
	where ORIGINID in
	(
	 SELECT gi2.UUID 
     from sde.gdb_items gi2 
     where gi2.physicalname in
      (select 
	   cr3.owner||'.'||cr3.table_name 
	   from sde.column_registry cr3 
	   where cr3.column_name='FEEDERTYPE' 
	   and cr3.table_name<>'CIRCUITSOURCE'	  
	   minus
	   select 
	   cr5.owner||'.'||cr5.table_name 
	   from sde.column_registry cr5 
	   where cr5.column_name='FEEDERTYPE' 
	   and cr5.table_name<>'CIRCUITSOURCE' 
	   and cr5.table_name in (
	         select cr4.table_name 
			 from sde.column_registry cr4 
			 where cr4.column_name='CIRCUITID'
			 ) 
	   )
	 )
   and DESTID in 
    (	
     select typechecka.UUID from SDE.GDB_ITEMS typechecka where typechecka.TYPE in (select UUID from SDE.GDB_ITEMTYPES where NAME='Relationship Class')
    ) 
  ) b on 
  a.DESTID=b.DESTID
)  
group by SOURCE_ID,DEST_ID,REL_ID
) REL_CLASS
 on REL_CLASS.SOURCE_ID=SOURCEFC.UUID
join 
(
SELECT gi2.UUID,gi2.PHYSICALNAME 
  from sde.gdb_items gi2 
  where gi2.physicalname in
      (select 
	   cr3.owner||'.'||cr3.table_name 
	   from sde.column_registry cr3 
	   where cr3.column_name='FEEDERTYPE' 
	   and cr3.table_name<>'CIRCUITSOURCE'	  
	   minus
	   select 
	   cr5.owner||'.'||cr5.table_name 
	   from sde.column_registry cr5 
	   where cr5.column_name='FEEDERTYPE' 
	   and cr5.table_name<>'CIRCUITSOURCE' 
	   and cr5.table_name in (
	         select cr4.table_name 
			 from sde.column_registry cr4 
			 where cr4.column_name='CIRCUITID'
			 ) 
	  )
) DESTFC 
on REL_CLASS.DEST_ID=DESTFC.UUID
order by 1,2,3;
sql_stmt varchar2(2000);
feedertype_num number;
BEGIN
FOR tab_obj IN push_feeder_type_list LOOP
  sql_stmt:='update '||tab_obj.DEST_FC_NAME||' set FEEDERTYPE=3 where FEATUREID in (select OBJECTID from '||tab_obj.SOURCE_FC_NAME||' where feedertype=3)';
  dbms_output.put_line(sql_stmt);
  execute immediate sql_stmt;
  sql_stmt:='update '||tab_obj.DEST_FC_NAME||' set FEEDERTYPE=2 where FEATUREID in (select OBJECTID from '||tab_obj.SOURCE_FC_NAME||' where feedertype=2)';
  dbms_output.put_line(sql_stmt);
  execute immediate sql_stmt;
  sql_stmt:='update '||tab_obj.DEST_FC_NAME||' set FEEDERTYPE=1 where FEATUREID in (select OBJECTID from '||tab_obj.SOURCE_FC_NAME||' where feedertype=1)';
  dbms_output.put_line(sql_stmt);
  execute immediate sql_stmt;
  commit;
END LOOP;
END;
